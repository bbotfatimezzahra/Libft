#include"libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	if (!lst || !new)
		return ;
	tmp = *lst;
	if (!*lst)
		*lst = new;
	else
	{
		tmp = ft_lstlast(tmp);
		tmp->next = new;
	}
}
