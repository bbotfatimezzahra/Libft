#include"libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	size_t	length;

	length = ft_strlen(s);
	i = length;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *) &s[i]);
		i--;
	}
	return (NULL);
}
